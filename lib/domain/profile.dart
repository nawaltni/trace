class UserProfile {
  final String id;
  final String email;
  final String name;
  final String phone;
  final String address;
  final String city;
  final String state;

  UserProfile({
    required this.id,
    required this.email,
    required this.name,
    required this.phone,
    required this.address,
    required this.city,
    required this.state,
  });
}
